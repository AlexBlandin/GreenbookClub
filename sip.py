ips = ["127.0.0.1", "255.0.0.1", "137.44.1.20", "48.8.9.72", ".".join(str(x) for x in range(256))]
z_a = ord("z") - ord("a") # apparently I find it hard to remember az is 26 letters

for ip in ips:
  sip = []
  for byte in ip.split("."):
    b, s = int(byte), []
    if b < ord("a"):
      b += ord("a")
      if b > ord("z"):
        s.append("Z")
        b -= z_a
        if b > ord("z"):
          z, b = divmod(b, z_a)
          s.append(str(z))
          s.append(chr(b + ord("a")))
        else:
          s.append(chr(b))
      else:
        s.append(chr(b).upper())
      sip.append("".join(s))
    elif b > ord("z"):
      z, b = divmod(b, z_a)
      s.append(str(z))
      s.append(chr(b + ord("a")))
      sip.append("".join(s))
    else:
      sip.append(chr(b))
  print(ip, "=", ".".join(sip))

